import java.util.Arrays;

import java.util.Arrays;

public class ArraySortingExample {
    public static void main(String[] args) {
        int[] array = {5, 2, 9, 1, 3};
        
        // Sorting the array add comment 
        Arrays.sort(array);
        
        // Printing the sorted array
        System.out.println("Sorted array: " + Arrays.toString(array));
    }
}   